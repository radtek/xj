package com.gafis.xj.model;

public class GiftInfo {
	private String pkId;
	private String gafisCode;
	private String gafisName;
	private Double gafisPrice;
	private int gafisCount;
	private int gafisType;
	public String getPkId() {
		return pkId;
	}
	public void setPkId(String pkId) {
		this.pkId = pkId;
	}
	public String getGafisCode() {
		return gafisCode;
	}
	public void setGafisCode(String gafisCode) {
		this.gafisCode = gafisCode;
	}
	public String getGafisName() {
		return gafisName;
	}
	public void setGafisName(String gafisName) {
		this.gafisName = gafisName;
	}
	public Double getGafisPrice() {
		return gafisPrice;
	}
	public void setGafisPrice(Double gafisPrice) {
		this.gafisPrice = gafisPrice;
	}
	public int getGafisCount() {
		return gafisCount;
	}
	public void setGafisCount(int gafisCount) {
		this.gafisCount = gafisCount;
	}
	public int getGafisType() {
		return gafisType;
	}
	public void setGafisType(int gafisType) {
		this.gafisType = gafisType;
	}
	
}
